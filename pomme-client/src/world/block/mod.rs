/// Compact per-state property map: `(key, value)` pairs sorted by key. States
/// have at most ~10 properties, so binary search on a boxed slice beats a
/// `HashMap` in both footprint and lookup cost.
pub struct PropMap(Box<[(&'static str, &'static str)]>);

impl PropMap {
    fn from_pairs(mut pairs: Vec<(&'static str, &'static str)>) -> Self {
        pairs.sort_unstable_by_key(|&(k, _)| k);
        Self(pairs.into_boxed_slice())
    }

    pub fn get(&self, key: &str) -> Option<&'static str> {
        self.0
            .binary_search_by_key(&key, |&(k, _)| k)
            .ok()
            .map(|i| self.0[i].1)
    }

    pub fn entries(&self) -> impl Iterator<Item = (&'static str, &'static str)> + '_ {
        self.0.iter().copied()
    }
}

/// The two behavior fields the client reads (from vanilla `BlockBehaviour`).
#[derive(Clone, Copy)]
pub struct BlockBehavior {
    pub destroy_time: f32,
    pub requires_correct_tool_for_drops: bool,
}

const DEFAULT_BEHAVIOR: BlockBehavior = BlockBehavior {
    destroy_time: 0.0,
    requires_correct_tool_for_drops: false,
};

/// A face-occlusion shape projected onto the face plane as a 16x16 bit grid;
/// `mask[v]` holds row `v`'s bits. Generated by `just lightgen`.
pub type FaceMask = [u16; 16];

/// Per-state light properties (vanilla `BlockBehaviour.BlockStateBase`'s
/// baked light cache), loaded from the per-version table generated by
/// `tools/lightgen` running vanilla's own code.
#[derive(Clone, Copy)]
pub struct LightProps {
    pub emission: u8,
    pub dampening: u8,
    /// Unconsumed at runtime (vanilla only folds it into `dampening` at
    /// bake time); carried for parity with the vanilla property set.
    #[allow(dead_code)]
    pub propagates_skylight_down: bool,
    pub can_occlude: bool,
    pub use_shape_for_light_occlusion: bool,
    /// Per-face occlusion masks in vanilla `Direction` ordinal order (DOWN,
    /// UP, NORTH, SOUTH, WEST, EAST); `Some` exactly when `can_occlude &&
    /// use_shape_for_light_occlusion`.
    pub face_occlusion: Option<&'static [FaceMask; 6]>,
}

/// Unknown states occlude fully, matching vanilla's bedrock fallback for
/// out-of-range light-engine reads.
const BEDROCK_LIGHT: LightProps = LightProps {
    emission: 0,
    dampening: 15,
    propagates_skylight_down: false,
    can_occlude: true,
    use_shape_for_light_occlusion: false,
    face_occlusion: None,
};

#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum FluidKind {
    Empty,
    Water,
    Lava,
}

/// Fluid content of a block state, matching vanilla `FluidState`: sources and
/// waterlogged blocks are amount 8; flowing fluid is `8 - level`; falling
/// fluid (level >= 8) renders/behaves as a full column.
#[derive(Clone, Copy)]
pub struct Fluid {
    pub kind: FluidKind,
    pub amount: u8,
    /// Not consumed yet; kept for vanilla parity (falling columns behave as
    /// full sources in flow/height logic).
    #[allow(dead_code)]
    pub falling: bool,
}

const NO_FLUID: Fluid = Fluid {
    kind: FluidKind::Empty,
    amount: 0,
    falling: false,
};

const FULL_WATER: Fluid = Fluid {
    kind: FluidKind::Water,
    amount: 8,
    falling: false,
};

impl Fluid {
    pub fn height(&self) -> f32 {
        self.amount as f32 / 9.0
    }
}

/// Per-state block data loaded from the per-version table generated by
/// `tools/blockgen` from Mojang's data-generator report. Hot paths (meshing
/// classifies and textures every block) index this directly.
struct BlockData {
    id: &'static str,
    properties: PropMap,
    behavior: BlockBehavior,
    /// Collision shape; see `block_shape::partial_shape` for the encoding.
    shape: Option<Box<[LocalBox]>>,
    is_air: bool,
    /// Vanilla `hasCollision`; precomputed so physics avoids name lookups.
    collides: bool,
    fluid: Fluid,
    light: LightProps,
}

#[derive(serde::Deserialize)]
struct BlockFile {
    version: String,
    state_count: u32,
    blocks: Vec<BlockEntry>,
}

// The data files also carry a `default_id` per block; nothing client-side
// consumes it yet, so serde skips it.
#[derive(serde::Deserialize)]
struct BlockEntry {
    name: String,
    first_id: u32,
    /// `(key, values)` in state-enumeration order; the last property varies
    /// fastest across consecutive state ids.
    #[serde(default)]
    props: Vec<(String, Vec<String>)>,
}

#[derive(serde::Deserialize)]
struct BehaviorEntry {
    destroy_time: f32,
    requires_correct_tool: bool,
}

#[derive(serde::Deserialize)]
struct LightFile {
    version: String,
    state_count: u32,
    /// Deduped face masks as 64-hex-char strings (16 rows of 4 chars).
    masks: Vec<String>,
    blocks: Vec<LightEntry>,
}

/// Per-block light fields; each is a scalar when uniform across the block's
/// states, else one value per state. `f` is 6 mask-dictionary indices —
/// a single tuple when uniform, else a per-state list of tuple-or-null.
#[derive(serde::Deserialize)]
struct LightEntry {
    name: String,
    e: ScalarOrPerState,
    d: ScalarOrPerState,
    p: ScalarOrPerState,
    o: ScalarOrPerState,
    u: ScalarOrPerState,
    #[serde(default)]
    f: Option<serde_json::Value>,
}

#[derive(serde::Deserialize)]
#[serde(untagged)]
enum ScalarOrPerState {
    One(u8),
    Many(Vec<u8>),
}

impl ScalarOrPerState {
    fn get(&self, offset: usize) -> u8 {
        match self {
            Self::One(v) => *v,
            Self::Many(vs) => vs[offset],
        }
    }
}

